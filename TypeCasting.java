public class TypeCasting {
    public static void main(String[] args) {
        double d=9.78;//explicit conversion
        int num=(int)d;
        System.out.println("double value before conversion:"+d);
        System.out.println("After explicit type conversion:"+num);
        int i=10;//implicit conversion
        double h=i;
        System.out.println("int value before conversion:"+i);
        System.out.println("After implicit type coversion:"+h);


    }
    
}
